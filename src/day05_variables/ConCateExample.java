package day05_variables;

public class ConCateExample {
    public static void main(String[] args) {

        System.out.println(5 + 4);              // 9
        System.out.println(5 + 4 + "Hello");    // 9Hello
        //int + int + String
        // 9 (int) + String     // String - 9Hello

        System.out.println("Hello" + 5 + 4);   // Hello54
        //String + int + int
        //    String   + int    -- Hello5 + 4
        //          String      -- Hello54

    }
    }
